package JavainterviewPractice;

public class PositiveNegativeNumber {

    public static void main(String[] args) {
        int a =25;
        if(a>0){
            System.out.println(" number" +a +" is positive number ");
        }else if(a<0){
            System.out.println(" number" +a +" is negative number ");
        }else if(a==0){
            System.out.println(" number" +a +" is zero number ");
        }

        double d = 2.3;
        if(d>0.0){
            System.out.println(" number" +d +" is positive number ");
        }else if(d<0.0){
            System.out.println(" number" +d +" is negative number ");
        }

    }
}
